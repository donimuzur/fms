Create procedure [dbo].[GetFuelCostData]
AS BEGIN

	MERGE FUEL_COST_BY_FUNC_REPORT_DATA AS Target
	USING (
		select SUM(FO.COST) AS TOTAL_FUEL_COST, FL.VEHICLE_TYPE, FL.VEHICLE_FUNCTION, FL.REGIONAL, 
		MONTH(FO.POSTED_TIME) AS REPORT_MONTH, YEAR(FO.POSTED_TIME) AS REPORT_YEAR, FO.POSTED_TIME 
		FROM MST_FUEL_ODOMETER FO JOIN MST_FLEET FL ON FO.EMPLOYEE_ID = FL.EMPLOYEE_ID
		GROUP BY FL.VEHICLE_TYPE, FL.VEHICLE_FUNCTION, FL.REGIONAL, FO.POSTED_TIME, MONTH(FO.POSTED_TIME)
	) AS Source
	
	On (Source.VEHICLE_TYPE = Target.VEHICLE_TYPE OR (Source.VEHICLE_TYPE IS NULL AND Target.VEHICLE_TYPE IS NULL))
	AND (Source.VEHICLE_FUNCTION = Target.[FUNCTION] OR (Source.VEHICLE_FUNCTION IS NULL AND Target.[FUNCTION] IS NULL))
	AND (Source.REGIONAL = Target.REGION OR (Source.REGIONAL IS NULL AND Target.REGION IS NULL))
	AND (Source.POSTED_TIME = Target.CREATED_DATE)
	WHEN NOT MATCHED BY TARGET THEN
		INSERT (TOTAL_FUEL_COST, VEHICLE_TYPE, [FUNCTION], REGION, REPORT_MONTH, REPORT_YEAR, CREATED_DATE)
		VALUES( Source.TOTAL_FUEL_COST, Source.VEHICLE_TYPE, Source.VEHICLE_FUNCTION, Source.REGIONAL, 
		MONTH(Source.POSTED_TIME), YEAR(Source.POSTED_TIME), Source.POSTED_TIME);

END